int hash_table[HASH_TABLE_SIZE];

int hash_code(const char* code) {
    return (code[0] - 'A') * 26 + (code[1] - 'A');
}

void cc_init() {
    for (int i = 0; i < HASH_TABLE_SIZE; i++) {
        hash_table[i] = -1;
    }

    for (int i = 0; i < NUM_CODES; i++) {
        int hash = hash_code(iso3166_codes[i]);
        if (hash_table[hash] == -1) {
            hash_table[hash] = i;
        }
    }
}

c_code cc_to_idx(const char* code) {
    int hash = hash_code(code);
    if (hash_table[hash] == -1) {
        return -1;
    }
    return hash_table[hash];
}

const char* cc_from_idx(c_code index) {
    if (index >= 0 && index < NUM_CODES) {
        return iso3166_codes[index];
    } else {
        return NULL;
    }
}